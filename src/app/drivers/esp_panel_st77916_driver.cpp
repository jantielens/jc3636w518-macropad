ESPPanel_ST77916_Driver::ESPPanel_ST77916_Driver()
    : backlight(nullptr), lcd(nullptr), currentBrightness(100), backlightIsOn(false),
      currentX(0), currentY(0), currentW(0), currentH(0),
        swapBuf(nullptr), swapBufCapacityPixels(0) {
        busMutex = xSemaphoreCreateMutex();
}

ESPPanel_ST77916_Driver::~ESPPanel_ST77916_Driver() {
    if (busMutex) {
        vSemaphoreDelete(busMutex);
        busMutex = nullptr;
    }
    if (swapBuf) {
        heap_caps_free(swapBuf);
        swapBuf = nullptr;
    }
    if (lcd) {
        delete lcd;
        lcd = nullptr;
    }
    if (backlight) {
        delete backlight;
        backlight = nullptr;
    }
}

void ESPPanel_ST77916_Driver::init() {
    Logger.logLine("ESP_Panel: Initializing ST77916 QSPI display");

    // Backlight PWM (LEDC), mirrored from sample.
    ledc_timer_config_t ledc_timer = {
        .speed_mode = LEDC_LOW_SPEED_MODE,
        .duty_resolution = LEDC_TIMER_13_BIT,
        .timer_num = LEDC_TIMER_0,
        .freq_hz = 5000,
        .clk_cfg = LEDC_AUTO_CLK,
    };
    ESP_ERROR_CHECK(ledc_timer_config(&ledc_timer));

    ledc_channel_config_t ledc_channel = {
        .gpio_num = (LCD_BL_PIN),
        .speed_mode = LEDC_LOW_SPEED_MODE,
        .channel = LEDC_CHANNEL_0,
        .intr_type = LEDC_INTR_DISABLE,
        .timer_sel = LEDC_TIMER_0,
        .duty = 0,
        .hpoint = 0,
    };
    ESP_ERROR_CHECK(ledc_channel_config(&ledc_channel));

    backlight = new ESP_PanelBacklight(ledc_timer, ledc_channel);
    backlight->begin();
    backlight->off();
    backlightIsOn = false;

    // Display bus (QSPI)
    auto* panel_bus = new ESP_PanelBus_QSPI(TFT_CS, TFT_SCK, TFT_SDA0, TFT_SDA1, TFT_SDA2, TFT_SDA3);
    panel_bus->configQspiFreqHz(TFT_SPI_FREQ_HZ);
    panel_bus->begin();

    // ST77916 panel
    lcd = new ESP_PanelLcd_ST77916(panel_bus, 16, TFT_RST);
    lcd->configVendorCommands(lcd_init_cmd, sizeof(lcd_init_cmd) / sizeof(lcd_init_cmd[0]));
    lcd->init();
    lcd->reset();
    lcd->begin();

    // Allocate a reusable swap buffer for optional byte swapping.
    // Size it to the LVGL draw buffer so we can swap+flush in one drawBitmap call.
    swapBufCapacityPixels = (uint32_t)LVGL_BUFFER_SIZE;
    swapBuf = (uint16_t*)heap_caps_malloc(sizeof(uint16_t) * swapBufCapacityPixels, MALLOC_CAP_INTERNAL | MALLOC_CAP_8BIT);

    Logger.logLine("ESP_Panel: Display initialized");
}

void ESPPanel_ST77916_Driver::setRotation(uint8_t rotation) {
    // ESP_PanelLcd supports rotation, but API differs across versions.
    // For now we keep the LCD at its default orientation; LVGL/touch handle rotation.
    (void)rotation;
}

void ESPPanel_ST77916_Driver::setBacklight(bool on) {
    if (!backlight) return;

    if (!on) {
        backlight->off();
        backlightIsOn = false;
        return;
    }

    backlight->setBrightness(clamp_percent(currentBrightness));
    backlightIsOn = true;
}

void ESPPanel_ST77916_Driver::setBacklightBrightness(uint8_t brightness) {
    if (!backlight) return;

    currentBrightness = clamp_percent(brightness);
    if (currentBrightness == 0) {
        backlight->off();
        backlightIsOn = false;
        return;
    }

    backlight->setBrightness(currentBrightness);
    backlightIsOn = true;
}

uint8_t ESPPanel_ST77916_Driver::getBacklightBrightness() {
    return currentBrightness;
}

bool ESPPanel_ST77916_Driver::hasBacklightControl() {
    return true;
}

void ESPPanel_ST77916_Driver::applyDisplayFixes() {
    // Match the sample behavior.
    if (lcd) {
        lcd->invertColor(true);
        lcd->displayOn();
    }
}

void ESPPanel_ST77916_Driver::startWrite() {
    if (busMutex) {
        xSemaphoreTake(busMutex, portMAX_DELAY);
    }
}

void ESPPanel_ST77916_Driver::endWrite() {
    if (busMutex) {
        xSemaphoreGive(busMutex);
    }
}

void ESPPanel_ST77916_Driver::setAddrWindow(int16_t x, int16_t y, uint16_t w, uint16_t h) {
    currentX = x;
    currentY = y;
    currentW = w;
    currentH = h;
}

void ESPPanel_ST77916_Driver::pushColors(uint16_t* data, uint32_t len, bool swap_bytes) {
    if (!lcd || !data || currentW == 0 || currentH == 0) return;

    const uint32_t pixelCount = (uint32_t)currentW * (uint32_t)currentH;
    if (len < pixelCount) {
        // Defensive: don't read past LVGL's buffer if something goes wrong upstream.
        return;
    }

    // The ESP_Panel API expects a byte pointer.
    // If swap_bytes is requested, swap into a contiguous buffer and flush once.
    if (!swap_bytes || !swapBuf || swapBufCapacityPixels < pixelCount) {
        lcd->drawBitmap(currentX, currentY, currentW, currentH, (const uint8_t*)data);
        return;
    }

    for (uint32_t i = 0; i < pixelCount; i++) {
        uint16_t v = data[i];
        swapBuf[i] = (uint16_t)((v << 8) | (v >> 8));
    }
    lcd->drawBitmap(currentX, currentY, currentW, currentH, (const uint8_t*)swapBuf);
}
